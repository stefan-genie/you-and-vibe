export type TaskType = "static" | "fakeChat" | "comparison" | "realChat" | "chatIde";
export type Difficulty = "easy" | "medium" | "hard";

export interface StaticContent {
  pages: string[];
}
export interface ComparisonPair {
  bad: string;
  good: string;
  note: string;
}
export interface ComparisonPage {
  title: string;
  pairs: ComparisonPair[];
}
export interface ComparisonContent {
  pages: ComparisonPage[];
}
export interface FakeChatContent {
  prompt: string;
  reply: string;
}
export interface RealChatContent {
  hasJudgeStep: boolean;
  intro: string;
}
export interface ChatIdeContent {
  intro: string;
  hints: string[];
}

export type TaskContent =
  | StaticContent
  | ComparisonContent
  | FakeChatContent
  | RealChatContent
  | ChatIdeContent
  | null;

export interface Task {
  id: string;
  type: TaskType;
  difficulty?: Difficulty;
  title: string;
  unlocksAfter: string | null;
  content: TaskContent;
}

